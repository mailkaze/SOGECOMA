    private void cargarDatos(){
        if (clases.SOGECOMA.cargarDatos){
            txtFecha.setText(clases.SOGECOMA.fechaHoraTrans.substring(0, 10));
            try{
                txtHora.setText(clases.SOGECOMA.fechaHoraTrans.substring(11,16));
            }catch(Exception e){
                txtHora.setText(clases.SOGECOMA.fechaHoraTrans.substring(11,15));
            }
            txtMaterial.setText(clases.SOGECOMA.nomMaterial);
            txtUnidad.setText(clases.SOGECOMA.udMaterial);
            txtCantidad.setText(String.valueOf(clases.SOGECOMA.cantTrans));
//            txtContratistaOrigen.setText(clases.SOGECOMA.contratistaEnt);
            txtCodigoItemOrigen.setText(clases.SOGECOMA.numItemOrigen);
            txtNombreItemOrigen.setText(clases.SOGECOMA.nomItemOrigen);
            cboBloqueOrigen.setSelectedItem(clases.SOGECOMA.bloqueOrigen);
//            txtContratistaDestino.setText(clases.SOGECOMA.contratistaEnt);
            txtCodigoItemDestino.setText(clases.SOGECOMA.numItemDestino);
            txtNombreItemDestino.setText(clases.SOGECOMA.nomItemDestino);
            cboBloqueDestino.setSelectedItem(clases.SOGECOMA.bloqueDestino);
            cmdGuardar.setVisible(false);
            habilitar=false;
        }else{
            cmdModificar.setVisible(false);
            cmdEliminar.setVisible(false);
            habilitar=true;
        }
    }
