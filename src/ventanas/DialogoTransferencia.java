    private void cboBloqueDestinoPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_cboBloqueDestinoPopupMenuWillBecomeInvisible
        //Busca en el cuadro Contratístas el nombre del tío de este bloque y lo coloca en el cuadro de texto
        //Da valor a la variable ID_Contratista
        try{
            POIFSFileSystem fs = new POIFSFileSystem(new FileInputStream("SOGECOMA.xls"));
            HSSFWorkbook libro = new HSSFWorkbook(fs);
            HSSFSheet cont = libro.getSheetAt(2);
            int numConts = cont.getLastRowNum();

            for (int c=1;c<=numConts;c++){
                if (cboBloqueDestino.getSelectedItem().toString().equals(cont.getRow(c).getCell(1).getStringCellValue())){
                    clases.SOGECOMA.ID_Contratista= (int)cont.getRow(c).getCell(0).getNumericCellValue();
                    txtContratistaDestino.setText(cont.getRow(c).getCell(2).getStringCellValue());
                }
            }
        }catch(Exception e){}
    }//GEN-LAST:event_cboBloqueDestinoPopupMenuWillBecomeInvisible

