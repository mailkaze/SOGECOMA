    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
        clases.ValidacionCampos valCamp = new clases.ValidacionCampos();
        if(valCamp.validarCampos(txtCantidad.getText(), 2)&& valCamp.validarCampos(txtMaterial.getText(), 0)
            && valCamp.validarCampos(txtUnidad.getText(), 0) && valCamp.validarCampos(txtNombreItemOrigen.getText(), 0)
            && valCamp.validarCampos(txtCodigoItemOrigen.getText(), 0) && valCamp.validarCampos(txtContratistaOrigen.getText(), 0)){
                if (valCamp.validarCampos(txtHora.getText(), 3)){
                    try {
                        clases.SOGECOMA.cantTrans=Double.valueOf(txtCantidad.getText());
                        clases.SOGECOMA.bloqueOrigen=cboBloqueOrigen.getSelectedItem().toString();
                        clases.SOGECOMA.bloqueDestino=cboBloqueDestino.getSelectedItem().toString();
                        clases.SOGECOMA.fechaHoraTrans=txtFecha.getText()+" "+txtHora.getText();

                        if (esNuevo){
                            if (JOptionPane.showConfirmDialog(rootPane, 
                            "¿Desea Transferir "+txtCantidad.getText()+" "+txtUnidad.getText()+" de "+txtMaterial.getText()+
                            " \nDESDE el Item "+txtCodigoItemOrigen.getText()+" del bloque "+cboBloqueOrigen.getSelectedItem().toString()+
                            " \nAL Item "+txtCodigoItemDestino.getText()+" del bloque "+cboBloqueDestino.getSelectedItem().toString()+"? (Revise bien los datos)") == 0) {
                                clases.GuardarNuevaTransferencia guarTrans=new clases.GuardarNuevaTransferencia();
                                guarTrans.guardarTransferencia();
                            }
                        } else {
                            if (JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de querer modificar esta Transferencia?") == 0) {
                                clases.ModificarEntrega modEnt=new clases.ModificarEntrega();
                                modEnt.modificarEntrega();
                            }
                        }
                        this.setVisible(false);
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(DialogoNuevoContratista.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(DialogoNuevoContratista.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
        }else{
            txtCantidad.setText("");
        }
    }//GEN-LAST:event_cmdGuardarActionPerformed

