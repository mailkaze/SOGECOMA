    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
        clases.ValidacionCampos valCamp = new clases.ValidacionCampos();
        if(valCamp.validarCampos(txtCantidad.getText(), 2)&& valCamp.validarCampos(txtMaterial.getText(), 0)
           && valCamp.validarCampos(txtUnidad.getText(), 0) && valCamp.validarCampos(txtNombreItem.getText(), 0)
           && valCamp.validarCampos(txtCodigoItem.getText(), 0) && valCamp.validarCampos(txtContratista.getText(), 0)){
            if (valCamp.validarCampos(txtAlmacenero.getText(), 0)){
                if (valCamp.validarCampos(txtHora.getText(), 3)){
                    try {
                        clases.SOGECOMA.almacenEnt=cboAlmacen.getSelectedItem().toString();
                        clases.SOGECOMA.almaceneroEnt=txtAlmacenero.getText();
                        clases.SOGECOMA.contratistaEnt=txtContratista.getText();
                        clases.SOGECOMA.cantEnt=Double.valueOf(txtCantidad.getText());
                        clases.SOGECOMA.bloqueEnt=cboBloque.getSelectedItem().toString();
                        //Conversión de String a Calendar:
//                        String strFormato="dd/MM/yyyy hh:mm";
//                        DateFormat formatoFecha = new SimpleDateFormat(strFormato);
//                        Date fecha = new Date();
//                        Calendar calendario=new GregorianCalendar();
//                        try{
//                            //Aquí ocurre la magia:
//                            fecha = formatoFecha.parse(txtFecha.getText()+" "+txtHora.getText());
//                            calendario.setTime(fecha);
//                        }catch (ParseException e){
//                        }
                        //Fin de la conversión de String a Calendar
                        clases.SOGECOMA.fechaHoraEnt=txtFecha.getText()+" "+txtHora.getText();

                        if (esNuevo){
                            clases.GuardarNuevaEntrega guarEnt=new clases.GuardarNuevaEntrega();
                            guarEnt.guardarEntrega();
                        } else {
                            if (JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de querer modificar esta Entrega?") == 0) {
                                clases.ModificarEntrega modEnt=new clases.ModificarEntrega();
                                modEnt.modificarEntrega();
                            }
                        }
                        this.setVisible(false);
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(DialogoNuevoContratista.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(DialogoNuevoContratista.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }else{
            txtCantidad.setText("");
        }
    }//GEN-LAST:event_cmdGuardarActionPerformed

