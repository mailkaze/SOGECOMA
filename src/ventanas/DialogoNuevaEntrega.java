    private void cargarDatos(){
        if (clases.SOGECOMA.cargarDatos){
            txtFecha.setText(clases.SOGECOMA.fechaHoraEnt.substring(0, 10));
            try{
                txtHora.setText(clases.SOGECOMA.fechaHoraEnt.substring(11,16));
            }catch(Exception e){
                txtHora.setText(clases.SOGECOMA.fechaHoraEnt.substring(11,15));
            }
            cboAlmacen.setSelectedItem(clases.SOGECOMA.almacenEnt);
            txtMaterial.setText(clases.SOGECOMA.nomMaterial);
            txtUnidad.setText(clases.SOGECOMA.udMaterial);
            txtCantidad.setText(String.valueOf(clases.SOGECOMA.cantEnt));
            txtContratista.setText(clases.SOGECOMA.contratistaEnt);
            txtAlmacenero.setText(clases.SOGECOMA.almaceneroEnt);
            txtCodigoItem.setText(clases.SOGECOMA.numItem);
            txtNombreItem.setText(clases.SOGECOMA.nomItem);
            cboBloque.setSelectedItem(clases.SOGECOMA.bloqueEnt);
            cmdGuardar.setVisible(false);
            habilitar=false;
        }else{
            cmdModificar.setVisible(false);
            cmdEliminar.setVisible(false);
            habilitar=true;
            txtAlmacenero.setText("Jhonny Vargas");
        }
    }
