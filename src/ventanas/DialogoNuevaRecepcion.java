    private void cargarDatos(){
        if (clases.SOGECOMA.cargarDatos){
            txtFecha.setText(clases.SOGECOMA.fechaHoraRec.substring(0, 10));
            try{
                txtHora.setText(clases.SOGECOMA.fechaHoraRec.substring(11,16));
            }catch(Exception e){
                txtHora.setText(clases.SOGECOMA.fechaHoraRec.substring(11,15));
            }
            cboAlmacen.setSelectedItem(clases.SOGECOMA.almacenRec);
            txtMaterial.setText(clases.SOGECOMA.nomMaterial);
            txtUnidad.setText(clases.SOGECOMA.udMaterial);
            txtCantidad.setText(String.valueOf(clases.SOGECOMA.cantRec));
            txtProveedor.setText(clases.SOGECOMA.proveedorRec);
            txtAlmacenero.setText(clases.SOGECOMA.almaceneroRec);
            cmdGuardar.setVisible(false);
            habilitar=false;
        }else{
            cmdModificar.setVisible(false);
            cmdEliminar.setVisible(false);
            habilitar=true;
            txtAlmacenero.setText("Jhonny Vargas");
        }
    }
    private void habilitarDeshabilitar(Boolean b){
        txtFecha.setEditable(b);
        txtHora.setEditable(b);
        cboAlmacen.setEnabled(b);
        cmdSeleccionarMaterial.setEnabled(b);
        txtCantidad.setEditable(b);
        txtProveedor.setEditable(b);
        txtAlmacenero.setEditable(b);
    }
