    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
        clases.ValidacionCampos valCamp = new clases.ValidacionCampos();
        if(valCamp.validarCampos(txtCantidad.getText(), 2)&& valCamp.validarCampos(txtMaterial.getText(), 0)
           && valCamp.validarCampos(txtUnidad.getText(), 0) && valCamp.validarCampos(txtProveedor.getText(), 0)
           && valCamp.validarCampos(txtAlmacenero.getText(), 0) && valCamp.validarCampos(txtHora.getText(), 3)){
                try {
                    clases.SOGECOMA.almacenRec=cboAlmacen.getSelectedItem().toString();
                    clases.SOGECOMA.almaceneroRec=txtAlmacenero.getText();
                    clases.SOGECOMA.proveedorRec=txtProveedor.getText();
                    clases.SOGECOMA.cantRec=Double.valueOf(txtCantidad.getText());
                    //Conversión de String a Calendar:
//                        String strFormato="dd/MM/yyyy hh:mm";
//                        DateFormat formatoFecha = new SimpleDateFormat(strFormato);
//                        Date fecha = new Date();
//                        Calendar calendario=new GregorianCalendar();
//                        try{
//                            //Aquí ocurre la magia:
//                            fecha = formatoFecha.parse(txtFecha.getText()+" "+txtHora.getText());
//                            calendario.setTime(fecha);
//                        }catch (ParseException e){
//                        }
                    //Fin de la conversión de String a Calendar
                    clases.SOGECOMA.fechaHoraRec=txtFecha.getText()+" "+txtHora.getText();
                    if (esNuevo) {
                        clases.GuardarNuevaRecepcion guarRec=new clases.GuardarNuevaRecepcion();
                        guarRec.guardarRecepcion();
                    } else {
                        if (JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de querer modificar esta Recepción?") == 0) {
                            clases.ModificarRecepcion modRec=new clases.ModificarRecepcion();
                            modRec.modificarRecepcion();
                        }
                    }
                    this.setVisible(false);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(DialogoNuevoContratista.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(DialogoNuevoContratista.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_cmdGuardarActionPerformed

